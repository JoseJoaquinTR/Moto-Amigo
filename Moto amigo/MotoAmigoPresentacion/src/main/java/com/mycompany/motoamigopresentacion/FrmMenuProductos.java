    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        cerrarSubPantallaActiva();
        
        dlgBuscar = new DlgBuscarProductos(this, true);
        dlgBuscar.setVisible(true);

        ProductoDTO productoSeleccionado = dlgBuscar.getProductoSeleccionado();

        if (productoSeleccionado == null) {
            return;
        }

        frmEliminar = new FrmEliminarProducto(productoSeleccionado);
        frmEliminar.setVisible(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

