    /**
     * Aplica el estilo redondeado a los botones del menú.
     */
    private void estilarBotones() {
        utileriasBotones.btnRedondeado(btnEliminar, "blanco", 16);
        utileriasBotones.btnRedondeado(btnEditar, "blanco", 16);
        utileriasBotones.btnRedondeado(btnCrear, "negro", 16);
        utileriasBotones.panelRedondeado(panelTarjeta, Color.WHITE, 18);


        Font fontBoton = new Font("Segoe UI", Font.BOLD, 18);
        btnEliminar.setFont(fontBoton);
        btnEditar.setFont(fontBoton);
        btnCrear.setFont(fontBoton);

        btnCrear.setForeground(Color.WHITE);
        btnEditar.setForeground(new Color(232, 100, 10));
        btnEliminar.setForeground(new Color(220, 53, 69));
        
        btnEliminar.setBackground(new Color(253,237,240));
        btnEditar.setBackground(new Color(255, 245, 235));
    }

    /**
     * Cierra la sub-pantalla que esté abierta. Se invoca antes de abrir una
     * nueva sub-pantalla para mantener la regla de mutua exclusión entre
     * sub-pantallas.
     */
    private void cerrarSubPantallaActiva() {
        if (frmCrear != null) {
            frmCrear.dispose();
            frmCrear = null;
        }
        if (frmEditar != null) {
            frmEditar.dispose();
            frmEditar = null;
        }
        if (frmEliminar != null) {
            frmEliminar.dispose();
            frmEliminar = null;
        }
        if (dlgBuscar != null) {
            dlgBuscar.dispose();
            dlgBuscar = null;
        }
    }

